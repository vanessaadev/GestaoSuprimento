                            <p class="img-fluid wow fadeInDown" data-wow-delay="0.3s"></i> A Gestão para Resultados nasce, portanto, com o objetivo de deslocar o foco dos meios – procedimentos e recursos – para colocá-lo, essencialmente, nos resultados transformadores de interesse do cidadão. Em síntese, isso significa pautar a ação governamental à busca de maior efetividade nas políticas públicas sem desconsiderar, também, a necessidade por maior eficiência e eficácia.</p>

                            <p>Se estabelece, assim, um novo paradigma com princípios que deverão guiar as ações institucionais e o comportamento dos agentes públicos, a saber: foco em públicos-alvo claros e inequívocos; orientação para resultados, numa perspectiva de longo prazo; flexibilidade e agilidade administrativa; valorização e comprometimento profissional com resultados; governança pública integrada, convergente e colaborativa; participação e controle social no governo; e sustentabilidade fiscal e financeira.</p>

                            <p>Os princípios são operacionalizados, na prática, por meio de quatro dimensões que agrupam em conjuntos processos sistemáticos a serem implementados pela GpR:</p>

