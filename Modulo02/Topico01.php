                            <p>Pode-se dizer, para simplificação do entendimento, que esse ciclo de gestão possui duas grandes fases: a de formulação e a de implementação. Trazendo para a nomenclatura da GpR, a fase de formulação engloba as atividades previstas no macroprocesso de Planejamento, envolvendo a elaboração de instrumentos de longo, médio e curto prazos, em níveis de Estado, setorial e organizacional; e de Orçamento, com os mecanismos legais instituídos por lei. A fase de implementação, por sua vez, é assistida pelas atividades inerentes aos macroprocessos de Monitoramento e Avaliação, fechando o ciclo de gestão.</p>


                            <p class="wow fadeIn" data-wow-delay="0.3s"></i> Desta forma, fica evidente a importância atribuída ao macroprocesso de Planejamento, pois este orienta a elaboração dos subsequentes. É por meio de seus produtos que se cria práticas de planejamento de médio e longo prazos, com visão integrada dos desafios de estado e de governo; define-se os indicadores e metas associados a políticas públicas, projetos e processos estratégicos, além de racionalizar a alocação de recursos.</p>



                            <p class="wow fadeIn" data-wow-delay="0.6s"></i> No âmbito governamental, são instituídos: a Estratégia de Desenvolvimento de Longo Prazo do Estado, o Plano Plurianual, de médio prazo, e o Acordo de Resultados, de curto prazo, que envolve também as setoriais. Além disso, há a estruturação da carteira de serviços e o planejamento de projetos de investimento, num nível mais programático.</p>




                            <p class="wow fadeIn" data-wow-delay="0.9s"></i> Na perspectiva setorial, a Agenda Estratégica Setorial é o principal instrumento norteador das políticas públicas setoriais no âmbito da gestão do Governo do Estado, e sua formulação consta como atividade do macroprocesso de Planejamento. Essencialmente, é o ponto de conexão entre a estratégia governamental e a das políticas. Promover o vínculo desses planos e o alinhamento intersetorial constituem num grande objetivo institucional que permeia diversos governos, e alcançá-lo é a missão que a Seplag adotou para si.</p>




