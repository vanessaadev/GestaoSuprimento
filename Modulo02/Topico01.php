                            <p>Em síntese, a implementação dos processos contidos nas quatro dimensões da GpR deve ser guiada pelos sete princípios norteadores e operacionalizados de acordo com os objetivos pretendidos em cada um dos estágios, que visam sanar problemas agudos de curto prazo, estabelecer a GpR em todas as secretarias e órgãos da administração pública estadual e, por fim, expandi-la aos demais poderes e aos principais municípios cearenses.</p>

                            <p>Esse arcabouço conceitual impacta na reconfiguração do Ciclo de Gestão Estratégica do Governo, criando, redefinindo e ajustando ações funcionais e temáticas, integradas de forma multissetorial nos seguintes macroprocessos:</p>


