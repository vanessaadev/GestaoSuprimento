                            <h2 class="Titulo">Alinhamento Estratégico</h2>
                            <hr>
                            <p>A efetivação das políticas públicas a partir da entrega de serviços inovadores ao cidadão, transformando a realidade social, é a busca constante do Governo do Estado do Ceará. Para isso, os órgãos da administração estadual direta e indireta têm o dever de realizar suas políticas setoriais guiadas por um eixo integrador, materializado por meio das prioridades e diretrizes definidas pela gestão governamental, levando em consideração as demandas socioeconômicas latentes da sociedade cearense.</p>


                            <p class="wow fadeInRight" data-wow-delay="0.3s"></i> Esse objetivo comum será a base para a concepção de programas e projetos nos quais o Governo aplicará prioritariamente seus escassos recursos, como os de capital, humano, cognitivo e políticos, por exemplo. Contudo, pode-se questionar, aqui, como cada proposta de intervenção ou projeto se enquadra na classe de prioridade da administração estadual, bem como se sua implementação de fato foi o suficiente para se atingir o objetivo.</p>


                            <p>Historicamente, diversas foram as respostas para as indagações acima. Aos estudiosos e especialistas dedicados à área de conhecimento que se propõe a analisar os avanços e descaminhos da gestão pública brasileira é sabido que, para cada momento histórico, um modelo de gestão norteou as ações governamentais – quando não, em momentos de transição, coexistiram. Os principais paradigmas que têm suportado os processos de reforma e modernização da administração pública no Brasil são os da administração patrimonial, da administração burocrática e, hoje, a Nova Gestão Pública.</p>

                            <p>A nova gestão pública no Ceará configura-se, com suas especificidades, na Gestão para Resultados (GpR). Embora no início dos anos 2000 o Governo experimentou uma tentativa de implementá-la, mas dificultada pela descontinuidade das gestões, é a partir de 2015, fortalecida por meio do Decreto Nº 32.216 de 2017, que o Poder Executivo compromete-se de fato com a transposição do eixo norteador de gestão dos insumos e serviços para os resultados transformadores esperados à população cearense.</p>

