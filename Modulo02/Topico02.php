                            <p>O fluxo interfederativo é a representação lógica do caminho pelo qual a informação percorre, no âmbito das três esferas de governo, para potencializar a formulação da Agenda Estratégica. Na prática, é o alinhamento necessário da política pública estadual com a nacional, fortalecendo a integração das ações setoriais.</p>

