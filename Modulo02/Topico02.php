                            <h2 class="Titulo">Agenda Estratégica Setorial: Conceitos</h2>
                            <hr>
                            <p>A Agenda Estratégica Setorial (AES) é um instrumento estratégico de formulação e promoção de política setorial de médio prazo integrado à estratégia de desenvolvimento governamental e articulado com os demais planos e ferramentas de gestão.</p>

                            <p class="img-fluid wow fadeInLeft" data-wow-delay="0.3s">Considerando os princípios do modelo de Gestão para Resultados (GpR) do Ceará, a Agenda Estratégica deve orientar-se por resultados numa perspectiva de longo prazo, valorizando a participação e controle social, o comprometimento profissional e a governança pública integrada, fundamentada na sustentabilidade fiscal e financeira e focada em públicos-alvo claros e inequívocos, praticando-a num ambiente administrativo flexível e ágil.</p>

                            <p>Desta forma, a concepção da Agenda deve cumprir com os objetivos propostos pelas quatro dimensões que congregam os processos necessários para a efetivação da Gestão para Resultados. As Agendas Estratégicas Setoriais são um produto da dimensão:</p>

                            <ul class="none Texto">
                                <li class="wow fadeInLeft" data-wow-delay="0.6s"><i class="fa fa-arrow-alt-circle-right CorVerdeClaroEstado"></i> <strong>Visão estratégica e escolha de prioridade:</strong> enquanto planejamento de médio prazo integrado aos desafios do Estado e do Governo e aos instrumentos de orçamentação. Além disso, focado em resultados, define indicadores e metas, projetos e processos estratégicos no âmbito da política setorial;</li>
                                <br>
                                <li class="wow fadeInLeft" data-wow-delay="0.9s"><i class="fa fa-arrow-alt-circle-right CorVerdeClaroEstado"></i> <strong>Monitoramento e avaliação sistemáticos:</strong> pois seu monitoramento e avaliação sistemáticos estimulam a disciplina na execução, geram informações estratégicas e estruturam novas políticas e projetos baseados em evidências;</li>
                                <br>
                                <li class="wow fadeInLeft" data-wow-delay="1.3s"><i class="fa fa-arrow-alt-circle-right CorVerdeClaroEstado"></i> <strong>Gestão e desenvolvimento de pessoas:</strong> porque sua formulação exige o estímulo de uma cultura de gestão orientada para resultados, seleção, motivação e formação contínua de gestores públicos, além de mecanismos de incentivos;</li>
                                <br>
                                <li class="wow fadeInLeft" data-wow-delay="1.6s"><i class="fa fa-arrow-alt-circle-right CorVerdeClaroEstado"></i> <strong>Governança orientada para resultados:</strong> com a elaboração de mecanismos de articulação e decisão intergovernamental e interinstitucional. A estrutura de governança deve permitir a participação e controle social, parcerias com o setor privado e sociedade civil e estimular a transparência por meio da comunicação social.</li>
                            </ul>


                            <!-- Importante -->
                            <div class="ImportanteCaixa">
                                <p class="ImportanteTitulo"> Importante</p>
                                <p class="ImportanteTexto">Contudo, a Agenda não se realiza apenas com as orientações da GpR. Mais do que isso, precisa-se entender quais as fontes e interfaces de interação necessárias para sua efetivação, que envolvem outros poderes e esferas. A formulação da Agenda Estratégica Setorial, portanto, observando os princípios supracitados, deve estar alinhada com as diretrizes de governo, no âmbito intrafederativo, e com as normas e direcionamentos das demais esferas de governo em respeito ao pacto federativo, no âmbito interfederativo.</p>
                            </div>
                            <br>

                            <p>Nesta perspectiva, as informações de cada fonte devem alimentar a Agenda de sua maneira, na figura de um sistema aberto, proporcionando um alinhamento estratégico. Este, entendido como uma iniciativa de se conceber a Agenda como parte de uma ação do Governo inserida num contexto de sistema. Ou seja, todos os processos de planejamento e gestão elaborados pelo poder público devem estar concatenados num conjunto de elementos que visa entregar à sociedade serviços integrados de melhor qualidade.</p>
                            <p>Assim, a Agenda deve ser orientada por diretrizes, normas, princípios e estratégias de dois fluxos de informação: interfederativo e intrafederativo.</p>

