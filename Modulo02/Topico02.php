                                <p class="ImportanteTitulo"> Importante</p>
                                <p class="ImportanteTexto">É importante citar, por fim, que alinhamentos, no âmbito internacional, são
                                    possíveis e fortemente recomendáveis. Um exemplo seria a observância do gestor aos Objetivos de Desenvolvimento Sustentável (ODS) enquanto subsídio para a construção da Agenda, muito embora já haja um direcionamento nacional em busca de se atingir esses objetivos.</p>
                            </div><br>
                            <p>Retomando a perspectiva do Governo do Ceará, a transformação da gestão, como mencionado anteriormente, não pode ocorrer repentinamente. Pelo contrário, é um processo baseado em estágios de implementação. Assim, a construção de Agendas Estratégicas Setoriais segue a mesma diretriz, e o atual modelo da GpR contempla, no segundo estágio, correspondente ao período de 2019 a 2022, o desafio de institucionalizar o planejamento estratégico setorial focalizado em resultados nas principais Secretarias e Órgãos da administração indireta, com atuação finalística.</p>
                            <p>Anteriormente a isso, contudo, já no primeiro estágio, que se findou em 2018, o governo se responsabilizou a implantar a gestão do conhecimento em áreas críticas para a GpR. Isso significa realizar duas atividades que contribuirão para esse objetivo, quais sejam (i) identificar áreas críticas e estabelecer objetivos para a gestão do conhecimento alinhados com as dimensões da Gestão para Resultados; e (ii) garantir que a gestão do conhecimento não seja um fim em si mesmo, mas um meio facilitador para o alcance de resultados.</p>

                            <!-- Fique Atento -->

                            <div class="FiqueAtentoCaixa">
                                <p class="FiqueAtentoTitulo"> Fique Atento</p>
                                <p class="FiqueAtentoTexto">A criação e disponibilização do Guia Metodológico de Formulação de Agenda Estratégica Setorial é justamente uma resposta do Governo do Estado aos desafios relativos à elaboração de planos de médio prazo, identificados no âmbito da gestão do conhecimento, e serve como um meio, não um fim em si mesmo, para alcançar maior efetividade na execução da estratégia setorial.</p>
                            </div><br>
                            <p>O supracitado instrumento metodológico versa sobre as diretrizes estruturais especificadas no material que implementa a Gestão para Resultados, no âmbito de todos os órgãos do Governo, para que a partir de 2019 as setoriais elaborem suas agendas. Abaixo, temos a síntese dessas orientações que fundamentam os elementos essenciais da Agenda:</p>

                            <ul class="none Texto">
                                <li class="wow fadeInDown" data-wow-delay="0.3s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> <strong>Preparar o planejamento setorial de médio prazo:</strong> Atividades de gerenciamento e planejamento, e envolve a definição de escopo, atores, cronograma, entre outras</li>
                                <br>
                                <li class="wow fadeInDown" data-wow-delay="0.6s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> <strong>Iniciar pela análise estratégica dos ambientes externo e interno à secretaria:</strong> Realizar estudos, pesquisas de opinião e qualitativas e envolver atores internos e externos à secretaria. Busca-se entender o passado e o presente da política</li>
                                <br>
                                <li class="wow fadeInDown" data-wow-delay="0.9s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> <strong>Formular, participativamente, a estratégia setorial:</strong> Realizar estudos e oficinas de trabalho (presencial ou virtual), envolvendo atores internos e externos a secretaria, para formular os produtos relativos ao futuro e os resultados desejados</li>
                                <br>
                                <li class="wow fadeInDown" data-wow-delay="1.2s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> <strong>Desdobrar o plano estratégico setorial:</strong> Formulação da estratégia do plano setorial, envolvida nas bases estratégicas (painel de indicadores e metas), e programática (através da carteira de projetos e processos estratégicos).</li>
                            </ul>

                            <br><p>Essas diretrizes se desdobram em processos que constituem a lógica estrutural de uma Agenda Estratégica Setorial. Isso significa dizer que o conjunto de atividades explicitadas acima se configuram nos elementos essenciais da Agenda.</p>

